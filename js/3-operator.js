// Оператори присвоєння
let t = new Date().getMinutes(); // Поточна хвилина;

console.log(`t = ${t}`);

// Збільшити значення t на 3

// Звичайний варіант
t = t + 3; // тотожно t += 3;

console.log(`t = t + 3 = ${t - 3} + 3 = ${t}`); // (t - 3) - для відображення значення перед додаванням

// Скорочений варіант
t += 3; // тотожно t = t + 3;

console.log(`t = t += 3 = ${t - 3} += 3 = ${t}`); // (t - 3) - для відображення значення перед додаванням

// Зменшити значення t на 2
t = t - 2; // тотожно t -= 3;

console.log(`t = t - 2 = ${t + 2} - 2 = ${t}`); // (t + 2) - для відображення значення перед додаванням

t -= 2; // тотожно t = t - 2;

console.log(`t = t -= 2 = ${t + 2} -= 2 = ${t}`); // (t + 2) - для відображення значення перед додаванням

// Помножити значення t на 4
t = t * 4; // Звичайний варіант
t *= 4; // Скорочений варіант

// Поділити значення t на 10
t = t / 10; // Звичайний варіант
t /= 10; // Скорочений варіант

console.log(`t = ${t}`);

// Оператори інкременту/декременту
// Постфіксний інкремент
let i = 8;

console.log(`i = ${i++}`); // читаємо значення "і", виводимо у консоль значення "і" (8),
// а потім збільшуємо значення "i" на 1: i = i + 1 | i += 1

