// "конкатенацію" (об'єднання, склеювання)
// Якщо один зі складових виразу НЕ рядок,
// тоді JS спробує перетворити його на рядок (неявне перетворення типів)

// Неявне перетворення типів
console.log(x + a); // "4210" : якщо "рядок + число", то число -> рядок
console.log(a + x); // "1042" : якщо "число + рядок", то число -> рядок
console.log(a * y); // 180 : якщо "число * рядок", то рядок (намагається) -> число, інакше NaN
console.log(y * a); // 180 : якщо "рядок * число", то рядок (намагається) -> число, інакше NaN
console.log(a / y); // 0.555 : якщо "число / рядок", то рядок (намагається) -> число, інакше NaN
console.log(y / a); //   1.8 : якщо "рядок / число", то рядок (намагається) -> число, інакше NaN
console.log(a - y); // -8 : якщо "число - рядок", то рядок (намагається) -> число, інакше NaN
console.log(y - a); //  8 : якщо "рядок - рядок", то рядок (намагається) -> число, інакше NaN

// Оператори присвоєння
let q = 5; // присвоюємо значення 5 для змінної q

