// Виведення чисел від 1 до 10 за допомогою циклу
// Оголошуємо змінну для циклу
// і задаємо початкове значення
let i = 1;

// Поки умова (i <= 10) буде виконуватися (повертати true)
// будемо повторювати код (набір інструкцій)
// в середині блоку між {}
// Якщо або КОЛИ умова поверне false - пропускаємо або завершуємо
// цикл відповідно
while (i <= 10) {
  // Початок блоку (тіла циклу)
  console.log(i);

  // Забезпечуємо вихід з циклу:
  // робимо можливим щоб умова стала хибною
  // інакше блок буде повторюватися "вічно"
  // і доведеться закривати браузер або перезавантажувати комп'ютер
  i += 1; // i = i + 1;

  // Кінець блоку (тіла циклу)
}

