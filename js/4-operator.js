const a = 10; // оголошення змінної з ініціалізацією
let b; // оголошення змінної

b = 5; // присвоєння значення 5 змінній b

// Арифметичні оператори
let c = a + b;

console.log(c); // 15

c = a - b;

console.log(c); // 5

// Не завжди потрібно створювати нові змінні
console.log(a * b, typeof (a * b)); // 50 number
console.log(`${a * b}`, typeof `${a * b}`); // '50' string

console.log(`${a} + ${b} = ${a + b}`);
console.log(`${a} - ${b} = ${a - b}`);
console.log(`${a} * ${b} = ${a * b}`);
console.log(`${a} / ${b} = ${a / b}`);

// Остача від ділення
console.log(`${a} % ${b} = ${a % b}`); // 0 : 10 = 5 + 5 + 0
console.log(`${b} % ${a} = ${b % a}`); // 5 = 10 - 5
console.log(`${a} % 3 = ${a % 3}`); // 1 : 10 = 3 + 3 + 3 + 1
console.log(`5 % 2 = ${5 % 2}`); // 1 - Непарне число
console.log(`4 % 2 = ${4 % 2}`); // 0 - Парне число

// Піднесення до степеня
console.log(`${a} ** ${b} = ${a ** b}`); // 100_000
console.log(`2 в степені 3 = ${2 ** 3}`); // 8
console.log(`10 в степені 2 = ${10 ** 2}`); // 100

// Для арифметичних операцій діє математичний пріоритет,
// операції можна групувати за допомогою дужок
console.log(`2 + 2 * 2 = ${2 + 2 * 2}`); // 6
console.log(`(2 + 2) * 2 = ${(2 + 2) * 2}`); // 8

