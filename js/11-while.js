// Гра: вгадати число, яке загадав комп'ютер від 0 до 10
// Задача: повторювати запит користувачу допоки він не вгадає
// або натисне Скасувати для завершення гри

// Math.random() -> від [0 до 1) -> 0.6118807830687223
// 0.6118807830687223 * 10 = 6.118807830687223
// Math.round(6.118807830687223) = 6
const compNumber = Math.round(Math.random() * 10);

console.log(`Число комп'ютера: `, compNumber);

// Для того щоб потрапити в тіло циклу
// використаємо трюк (лайфхак)
// Зробимо так, щоб на 1-у кроці умова (condition)
// завжди повертала true (виконувалася)

let userNumber = -1; // явно задаємо значення поза діапазоном [0, 10]

while (userNumber !== compNumber) {
  // Отримати відповідь від користувача

  // Створюємо нову змінну щоб уникнути зміни типу (number на string | null),
  // також назва змінної краще відображає вміст
  const userAnswer = prompt(
    "Введіть число від 0 до 10\nабо натисніть Скасувати щоб завершити гру",
  );

  // Тимчасове повідомлення для відлагодження
  console.log("Відповідь користувача:", userAnswer);

  // Перевірити введені дані
  // // Вийти або продовжити
  if (userAnswer === null) {
    // Користувач натиснув кнопку Скасувати
    alert("Гру скасовано!");
    // Виходимо з циклу
    break; // примусовий вихід з циклу
  }

  // Оскільки break завершує роботу циклу,
  // то можна обійтися без else

  // Перетворюємо відповідь користувача на число користувача
  userNumber = Number(userAnswer);

  // Тимчасове повідомлення для відлагодження
  console.log("Число користувача:", userNumber);

  // // Оцінити дані
  if (Number.isNaN(userNumber)) {
    alert("Ви ввели не число, повторіть спробу");
    continue; // зупиняє роботу циклу НА ПОТОЧНОМУ КРОЦІ
    // і починає нове (наступне) коло циклу
    // з перевірки умови while (userNumber !== compNumber)
  }

  // Привітати з перемогою чи почати нове коло
  if (userNumber === compNumber) {
    alert("Вітаємо з перемогою!\nУдача на вашому боці!");
    // Умова while (userNumber !== compNumber) стала хибною,
    // тому після досягнення кінця циклу
    // і перевірки умови відбудеться вихід з циклу
  } else {
    const diff = Math.abs(compNumber - userNumber);

    switch (diff) {
      case 1:
        alert("Дуже гаряче");
        break;
      case 2:
        alert("Гаряче");
        break;
      case 3:
        alert("Тепло");
        break;

      default:
        break;
    }
  }

  //   else if (userNumber > compNumber) {
  //     alert("Забагато");
  //   } else {
  //     // userNumber < compNumber
  //     alert("Замало");
  //   }
}

