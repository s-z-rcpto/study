// 3. Знайти суму чисел від 1 до n
// - Користувач вводить число n. +
// - Порахувати суму чисел від 1 до n. +
console.log("------ Завдання 3 ------");

const n = Number(
  prompt("Введіть число для підрахунку суми від 1 до вказаного числа", 5)
);

