console.log(`j = ${j}`);

// Нехай `xx = 10`.
// Збільшити його на 5 через `+=`, потім ще на 1 через `++`.
// Виведи результат.

let xx = 10;
console.log(`xx = ${xx}`);

xx += 5; // xx = xx + 5 = 10 + 5 = 15
console.log(`xx = ${xx}`);

xx++; // xx = xx += 1 = xx + 1 = 15 + 1 = 16
console.log(`xx = ${xx}`);

