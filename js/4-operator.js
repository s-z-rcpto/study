// Множення
let m = 4;

m = m * 5; // звичайний спосіб множення m на 5
m *= 5; // скорочений варіант множення m на 5

// Ділення змінної m на 2
m = m / 2; // звичайний спосіб
m /= 2; // скорочений варіант

// Оператори інкременту/декременту
let i = 8;

// Способи збільшення значення змінної на 1 - інкремент
i = i + 1; // звичайний спосіб (i = 8 + 1 = 9)
i += 1; // скорочений варіант (i = 9 + 1 = 10)
i++; // оператор інкременту (постфіксний) (i = 10 + 1 = 11)

// Принцип дії
// 1. Береться поточне значення змінної
// 2. Рахується вираз у правій частині
// 3. Результат записується у ліву частину (оновлюється значення змінної)

console.log(`i = ${i}`); // 11

// Постфіксний оператор
let j = 7;

console.log(`j = ${j++}`); // j = 7
// постфіксний оператор спочатку повертає поточне значення
// потім збільшує його на 1
console.log(`j = ${j}`); // j = 8

// Префіксний оператор
let k = 7;

console.log(`k = ${++k}`); // k = 8
// префіксний оператор спочатку збільшує поточне значення на 1
// потім повертає вже оновлене значення
console.log(`k = ${k}`); // k = 8
