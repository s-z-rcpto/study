// 5. Перевірка пароля
// - Задати правильний пароль +
// - Давати користувачу 3 спроби для введення +
// - При правильному введенні вивести "Доступ надано" +
// - Після третьої невдалої спроби вивести "Доступ заблоковано" +
console.log("------ Завдання 5 ------");

const correctPass = "qwerty";

let userPass;
let userTry = 0;

do {
  userPass = prompt("Введіть пароль");

  console.log(`Відповідь користувача:`, userPass);

  // Якщо користувач натиснув Скасувати - виходимо з циклу
  if (userPass === null) {
    break; // примусовий вихід з циклу
  }

  userTry = userTry + 1; // зараховуємо спробу введення пароля, оскільки не натиснув Скасувати

  if (userPass === correctPass) {
    alert("Доступ надано");
  } else if (userTry >= 3) {
    alert("Доступ заблоковано");
    break;
  }
} while (userPass !== correctPass);

// 6. Накопичення суми
// - Користувач вводить числа +
// - Програма додає їх до загальної суми +
// - Введення продовжується поки користувач не введе 0 +
// - Вивести кінцеву суму та кількість введених чисел +
console.log("------ Завдання 6 ------");

let sum6 = 0;
let count6 = 0;

while (true) {
  const userNumber = Number(prompt("Введіть число або 0 щоб завершити"));

  if (Number.isNaN(userNumber)) {
    alert("Помилка. Ввели не число");
    continue;
  }

  if (userNumber === 0) {
    break;
  }

  sum6 += userNumber; // sum6 = sum6 + userNumber;
  count6 += 1; // count6 = count6 + 1

  console.log("sum6 =", sum6, "count6 =", count6);
}

console.log(
  `Сума введених чисел: ${sum6}, кількість введених чисел: ${count6}`
);

