const fruits = ["Яблуко", "Банан", "Груша"];

console.log(fruits); // ['Яблуко', 'Банан', 'Груша']

// Додавання елементів до масиву
fruits.splice(1, 1, "Абрикос"); // додали "Абрикос" на позицію з індексом 1
// шукаємо елемент з індексом 1 (start)
// видаляємо 0 (deleteCount) елементів, починаючи з індексу 1 (start)
// додає елемент "Абрикос" у позицію 1 (start)

console.log(fruits); // ['Яблуко', 'Абрикос', 'Банан', 'Груша']

fruits.splice(2, 0, "Вишня", "Алича"); // додали кілька елементів ("Вишня", "Алича") на позицію з індексом 2

console.log(fruits); // ['Яблуко', 'Абрикос', 'Вишня', 'Алича', 'Банан', 'Груша']

// Видалення елементів з масиву
fruits.splice(2, 2); // видалили 2 елементи, починаючи з позиції з індексом 2

console.log(fruits); // ['Яблуко', 'Абрикос', 'Банан', 'Груша']

// Заміна елементів масиву
fruits.splice(1, 1, "Апельсин", "Мандарин"); // замінили елемент "Абрикос" на декілька елементів ("Апельсин", "Мандарин)
// шукаємо елемент з індексом 1 (start)
// видаляємо 1 (deleteCount) елемент ('Абрикос'), починаючи з індексу 1 (start)
// додаємо один або декілька елементів ("Апельсин", "Мандарин") у позицію 1 (start)

console.log(fruits); // ['Яблуко', 'Апельсин', 'Мандарин', 'Банан', 'Груша']

// Об'єднання масивів
const gorodina = ["Морква", "Буряк", "Картопля"];

console.log(gorodina);

const produkti = fruits.concat(gorodina); // створює новий масив, де елементами будуть
// елементи масиву fruits і елементи масиву gorodina
// fruits і gorodina залишаються без змін

console.log("produkti =", produkti);
console.log("fruits =", fruits);
console.log("gorodina =", gorodina);

// Спосіб 2
const produkti2 = [].concat(fruits, gorodina); // умовно, додали до порожнього масиву
// елементи масиву fruits і елементи масиву gorodina
// fruits і gorodina залишаються без змін
// повертається новий масив з об'єдними елементами

console.log("produkti2 =", produkti2);

// Спосіб 3
const produkti3 = [...fruits, ...gorodina]; // створюємо новий масив з об'єднання елементів
// масивів fruits і gorodina

console.log("produkti3 =", produkti3);

// Зміна порядку розташування елементів у масиві на протилежний
