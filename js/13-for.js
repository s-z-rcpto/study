for (let i = 0; i < 5; i++) {
  console.log(i);
} // виведення чисел від 0 до 4

for (let i = 1; i <= 5; i++) {
  console.log(i);
} // виведення чисел від 1 до 5

// Оголошуємо змінну циклу "i"
// та присвоюємо їй значення 1
//
// Задаємо умову виходу з циклу (i <= 5)
//
// Створюємо умови для того, щоб умова виходу
// не виконалася - задаємо зміну значення "i" (i++)
//
// Початок виконання циклу
//
// 1. Перевірка умови
// Якщо умова хибна - завершуємо виконання,
// переходимо до наступного етапу програми
//
// Якщо умова виконується (true), то переходимо
// до виконання тіла циклу
//
// 2. Виконання тіла циклу: однієї або багатьох інструкцій
//
// 3. Змінюємо значення змінної циклу (i++)
//
// 4. Перевіряємо умову, див. пункт 1 і так по колу

