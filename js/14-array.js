// Створення рядка з масиву
// 14. Дано масив ["Яблуко", "Банан", "Груша"].
// Використовуючи цикл, об’єднайте всі елементи масиву в один рядок, розділивши комами.
// Наприклад, "Яблуко, Банан, Груша".

const arr14 = ["Яблуко", "Банан", "Груша"];

// Спосіб 1
let result14 = "";

for (let index = 0; index < arr14.length; index++) {
  const element = arr14[index];

  if (index === 0) {
    result14 += element;
  } else {
    result14 += ", " + element;
  }
}

console.log(result14);

// Спосіб 2
console.log(arr14.join(", ")); // об'єднує елементи масиву в один рядок
// з розділенням за допомогою ", "

// Перевірка існування елемента в масиві

// 9+. Вивести елементи масиву, крім певного числа
// Дано масив [1, 2, 3, 4, 5, 6].
// Запитати користувача число.
// Використовуючи цикл і умовний оператор, перевірити чи є у масиві число, яке ввів користувач.

const arr9 = [1, 2, 3, 4, 5, 6];

// Спосіб 1
const userNumber = 8; // Number(prompt("Input number"));
let numberInArray = false;

for (const element of arr9) {
  if (element === userNumber) {
    numberInArray = true;
  }
}

console.log(`Наявність числа ${userNumber} в масиві ${arr9}:`, numberInArray);

// Спосіб 2
numberInArray = arr9.includes(userNumber); // перевіряє наявність значення змінної userNumber
// у масиві arr9, повертає true - якщо число в масиві, false - якщо числа немає

console.log(`Наявність числа ${userNumber} в масиві ${arr9}:`, numberInArray);

// Спосіб 3
const position9 = arr9.indexOf(userNumber); // шукає значення змінної userNumber
// у масиві arr9 і повертає (-1) - якщо числа немає в масиві, index - якщо число є в масиві
// (-1), тому що нумерація елементів масиву починається з 0,
// тобто якщо елемент який ми шукаємо знаходиться на першій позиції, то його індекс = 0,
// відповідно indexOf() поверне позицію елемента - 0

console.log(`Позиція числа ${userNumber} в масиві ${arr9}:`, position9);

numberInArray = position9 >= 0;

console.log(`Наявність числа ${userNumber} в масиві ${arr9}:`, numberInArray);
